                MOVU        r2          h00                     ...
                                                                ...
                MOVL        r3          hFF                     ... % Set register to some value.
                MOVU        r3          hFF                     ...
                                                                ...
                MOVL        r0          lsb(c.RAM_START + 0)    ... % Assign memory address.
